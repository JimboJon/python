mood = input("How are you feeling today? ")
if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("Here's a knife to slice open your wrists")
elif mood == "excited":
    print("Must be from all the porn you're watching.")
elif mood == "relaxed":
    print("Do you have more of those drugs?")
else:
    print("I don't recognize this mood")

