number1 = int(input("Choose a number "))
number2 = int(input("choose a second number "))
operation = input("choose what you wanna do with the numbers ")
if operation == "+":
    print(number1+number2)
if operation == "-":
    print(number1-number2)
if operation == "*":
    print(number1*number2)
if operation == "/":
    print(number1/number2)
